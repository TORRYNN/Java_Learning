public class BitwiseOperator {
    public static void main(String args[]) {
        // Binary and &
        System.out.println(5 & 6);

        // Binary OR |
        System.out.println(5 | 6);

        // Binary XOR ^
        System.out.println(5 ^ 6);

        // Binary One's Complement
        System.out.println(~1);

        // Binary Left Shift <<
        System.out.println(2 << 4);

        // Binary Right Shift <<
        System.out.println(2 >> 4);
    }
}
